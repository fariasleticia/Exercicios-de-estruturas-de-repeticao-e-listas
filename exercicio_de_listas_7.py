numeros_inteiros = [5, 61, 74, 23, 98]
soma_numeros = 0
multiplicar_numeros = 1

for valores in numeros_inteiros:
    soma_numeros = soma_numeros + valores
    multiplicar_numeros = multiplicar_numeros * valores

print("Soma dos números maneiros:", soma_numeros)
print("Multiplicação dos números maneiros:", multiplicar_numeros)
print("Saca só quais são os números legais:", numeros_inteiros)